makeCacheMatrix <-function(x=matrix()){
m<-NULL
set<-function(y) {
x<<-y
m<<-NULL
}
get<-function() x
setmean<-function(solve) m<<-mean
getmean<-function() m
list(set = set, get = get,
setmean = setmean,
getmean = getmean)
}

cachesolve<-function(x,....){
m<-x$getmean()
if(!is.null(m)){
message("getting cached data")
return(m)
}
data<-x$get()
m<-solve(data,...)
x$setmean(m)
m
}